// BuildStateIndexMap returns a map from each *ATNState to its array index in
// a.States. This is the stable runtime index used in generated states[i]
// references and by the parser generator for EnterRule/Sync calls.
func BuildStateIndexMap(a *ATN) map[*ATNState]int {
	idx := make(map[*ATNState]int, len(a.States))
	for i, s := range a.States {
		idx[s] = i
	}
	return idx
}

func renderCompletionHint(h *parser.CompletionHint) string {
	if h == nil {
		return "nil"
	}
	out := "&parser.CompletionHint{Field: " + strconv.Quote(h.Field)
	if h.PrecedingAction != nil {
		out += ", PrecedingAction: &parser.ActionInfo{TargetType: " +
			strconv.Quote(h.PrecedingAction.TargetType) +
			", Property: " + strconv.Quote(h.PrecedingAction.Property) + "}"
	}
	out += "}"
	return out
}

func EmitGoSource(pkgName, funcName, importPath string, rtn *ATN, grammr grammar.Grammar, tokenTypeVarNames []string) codegen.Node {
	idx := BuildStateIndexMap(rtn)
	elementNames := BuildElementNames(grammr)
	stateNames := BuildStateNameMap(rtn, elementNames)

	// nm returns the constant name for a given ATN state pointer.
	nm := func(s *ATNState) string {
		return stateNames[idx[s]]
	}

	n := codegen.NewNode()

	n.AppendLine("// Code generated by fastbelt. DO NOT EDIT.")
	n.AppendLine()
	n.AppendLine("package ", pkgName)

	n.AppendLine("import \"", importPath, "\"")
	n.AppendLine()

	// Unified const block: one constant per ATN state.
	// All three callers (atn_gen, parser_gen, completion_parser_gen)
	// reference names from this single list.
	n.AppendLine("const (")
	n.Indent(func(n codegen.Node) {
		n.AppendLine(stateNames[0], " = iota")
		for _, name := range stateNames[1:] {
			n.AppendLine(name)
		}
	})
	n.AppendLine(")")
	n.AppendLine()

	n.AppendLine("func ", funcName, "() *parser.RuntimeATN {")
	n.Indent(func(n codegen.Node) {
		n.AppendLine("states := make([]*parser.RuntimeATNState, ", strconv.Itoa(len(rtn.States)), ")")

		// Emit state declarations (without transitions so forward refs are fine).
		for i, s := range rtn.States {
			n.AppendLine("states[", stateNames[i], "] = &parser.RuntimeATNState{")
			n.Indent(func(n codegen.Node) {
				n.AppendLine("StateNumber: ", stateNames[i], ",")
				n.AppendLine("Type: parser.", atnStateTypeName(s.Type), ",")
				n.AppendLine("Decision: ", strconv.Itoa(s.Decision), ",")

				if s.EpsilonOnlyTransitions {
					n.AppendLine("EpsilonOnlyTransitions: true,")
				}
			})
			n.AppendLine("}")
		}

		// Emit transitions (states already declared, so pointer refs are valid).
		for i, s := range rtn.States {
			if len(s.Transitions) == 0 {
				continue
			}
			n.AppendLine("states[", stateNames[i], "].Transitions = []parser.RuntimeTransition{")
			n.Indent(func(n codegen.Node) {
				for _, t := range s.Transitions {
					switch at := t.(type) {
					case *AtomTransition:
						n.AppendLine(
							"parser.NewAtomTransition(states[",
							nm(at.Target()),
							"], ",
							tokenTypeVarNames[at.TokenTypeId],
							", ",
							renderCompletionHint(at.CompletionHint),
							"),",
						)
					case *EpsilonTransition:
						n.AppendLine("parser.NewEpsilonTransition(states[", nm(at.Target()), "]),")
					case *RuleTransition:
						n.AppendLine(
							"parser.NewRuleTransition(states[",
							nm(at.Target()),
							"], states[",
							nm(at.FollowState),
							"], ",
							renderCompletionHint(at.CompletionHint),
							"),",
						)
					}
				}
			})
			n.AppendLine("}")
		}

		// DecisionStates slice.
		n.AppendLine("decisionStates := make([]*parser.RuntimeATNState, ", strconv.Itoa(len(rtn.DecisionStates)), ")")
		for i, ds := range rtn.DecisionStates {
			if ds != nil {
				n.AppendLine("decisionStates[", strconv.Itoa(i), "] = states[", nm(ds), "]")
			}
		}

		// DecisionMap: indexed by Decision number, populated from DecisionStates.
		n.AppendLine("decisionMap := make([]*parser.RuntimeATNState, ", strconv.Itoa(len(rtn.DecisionStates)), ")")
		for i, ds := range rtn.DecisionStates {
			if ds != nil {
				n.AppendLine("decisionMap[", strconv.Itoa(i), "] = states[", nm(ds), "]")
			}
		}

		n.AppendLine("return parser.NewRuntimeATN(states, decisionStates, decisionMap)")
	})

	n.AppendLine("}")
	return n
}

func atnStateTypeName(t parser.ATNStateType) string {
	switch t {
	case parser.ATNInvalidType:
		return "ATNInvalidType"
	case parser.ATNBasic:
		return "ATNBasic"
	case parser.ATNRuleStart:
		return "ATNRuleStart"
	case parser.ATNRuleStop:
		return "ATNRuleStop"
	case parser.ATNBlockEnd:
		return "ATNBlockEnd"
	case parser.ATNLoopBack:
		return "ATNLoopBack"
	case parser.ATNLoopEntry:
		return "ATNLoopEntry"
	case parser.ATNLoopEnd:
		return "ATNLoopEnd"
	default:
		return fmt.Sprintf("ATNStateType(%d)", int(t))
	}
}
