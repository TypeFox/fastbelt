type LinkerGeneratorContext struct {
	grammar      grammar.Grammar
	fields       []LinkerField
	ifaceParents map[string][]string // interface name → direct parent interface names
}

type LinkerField struct {
	typeName string
	name     string
	target   string
}

func GenerateLinker(grammr grammar.Grammar, packageName string) string {
	context := generateContext(grammr)
	node := generator.NewNode()
	node.AppendLine("// Code generated by typefox.dev/fastbelt/cmd/fastbelt. DO NOT EDIT.")
	node.AppendLine()
	node.AppendLine("package ", packageName)
	node.AppendLine()
	node.AppendLine("import (")
	node.Indent(func(n generator.Node) {
		if len(context.fields) > 0 {
			n.AppendLine("\"context\"")
			n.AppendLine("\"slices\"")
		}
		n.AppendLine("\"reflect\"")
		n.AppendLine()
		n.AppendLine("core \"typefox.dev/fastbelt\"")
		if len(context.fields) > 0 {
			n.AppendLine("\"typefox.dev/fastbelt/linking\"")
			n.AppendLine("\"typefox.dev/fastbelt/util/extiter\"")
		}
		n.AppendLine("\"typefox.dev/fastbelt/util/service\"")
	})
	node.AppendLine(")")
	node.AppendLine()

	node.AppendNode(generateScopeProvider(context))
	node.AppendNode(generateLinker(context))
	node.AppendNode(generateReferenceConstructor(context))
	node.AppendNode(generateSymbolContainers(context))

	return FormatIfPossible(node.String())
}

func generateContext(grammr grammar.Grammar) *LinkerGeneratorContext {
	fields := []LinkerField{}
	for _, iface := range grammr.Interfaces() {
		for _, field := range iface.Fields() {
			if refType, ok := field.Type().(grammar.ReferenceType); ok {
				fields = append(fields, LinkerField{
					typeName: iface.Name(),
					name:     field.Name(),
					target:   refType.Type().Ref(context.TODO()).Name(),
				})
			} else if arrayType, ok := field.Type().(grammar.ArrayType); ok {
				if refType, ok := arrayType.InternalType().(grammar.ReferenceType); ok {
					fields = append(fields, LinkerField{
						typeName: iface.Name(),
						name:     field.Name(),
						target:   refType.Type().Ref(context.TODO()).Name(),
					})
				}
			}
		}
	}
	ifaceParents := map[string][]string{}
	for _, iface := range grammr.Interfaces() {
		parents := []string{}
		for _, ext := range iface.Extends() {
			parents = append(parents, ext.Ref(context.TODO()).Name())
		}
		ifaceParents[iface.Name()] = parents
	}
	return &LinkerGeneratorContext{
		grammar:      grammr,
		ifaceParents: ifaceParents,
		fields:       fields,
	}
}

func generateScopeProvider(context *LinkerGeneratorContext) generator.Node {
	node := generator.NewNode()
	node.AppendLine("type ", context.grammar.Name(), "ScopeProvider interface {")
	node.Indent(func(n generator.Node) {
		for _, field := range context.fields {
			n.AppendLine("Scope", field.typeName, field.name, "(ctx context.Context, reference *core.Reference[", field.target, "]) core.Scope")
		}
	})
	node.AppendLine("}")
	node.AppendLine()

	node.AppendLine("type Default", context.grammar.Name(), "ScopeProvider struct {")
	node.AppendLine("	sc *service.Container")
	node.AppendLine("}")
	node.AppendLine()

	node.AppendLine("func NewDefault", context.grammar.Name(), "ScopeProvider(sc *service.Container) ", context.grammar.Name(), "ScopeProvider {")
	node.AppendLine("	return &Default", context.grammar.Name(), "ScopeProvider{sc: sc}")
	node.AppendLine("}")
	node.AppendLine()

	for _, field := range context.fields {
		node.AppendLine("func (s *Default", context.grammar.Name(), "ScopeProvider) Scope", field.typeName, field.name, "(ctx context.Context, reference *core.Reference[", field.target, "]) core.Scope {")
		node.AppendLine("	return linking.DefaultScopeOfType[", field.target, "](reference.Owner())")
		node.AppendLine("}").AppendLine()
	}
	return node
}

func generateLinker(context *LinkerGeneratorContext) generator.Node {
	node := generator.NewNode()
	node.AppendLine("type ", context.grammar.Name(), "ReferenceLinker interface {")
	node.Indent(func(n generator.Node) {
		for _, field := range context.fields {
			n.AppendLine("Link", field.typeName, field.name, "(ctx context.Context, reference *core.Reference[", field.target, "]) (*core.SymbolDescription, *core.ReferenceError)")
		}
	})
	node.AppendLine("}")
	node.AppendLine()

	node.AppendLine("type Default", context.grammar.Name(), "ReferenceLinker struct {")
	node.AppendLine("	sc            *service.Container")
	node.AppendLine("	scopeProvider ", context.grammar.Name(), "ScopeProvider")
	node.AppendLine("}")
	node.AppendLine()

	node.AppendLine("func NewDefault", context.grammar.Name(), "ReferenceLinker(sc *service.Container) ", context.grammar.Name(), "ReferenceLinker {")
	node.AppendLine("	return &Default", context.grammar.Name(), "ReferenceLinker{sc: sc}")
	node.AppendLine("}")
	node.AppendLine()
	node.AppendLine("func (s *Default", context.grammar.Name(), "ReferenceLinker) scope() ", context.grammar.Name(), "ScopeProvider {")
	node.AppendLine("	if s.scopeProvider == nil {")
	node.AppendLine("		s.scopeProvider = service.MustGet[", context.grammar.Name(), "ScopeProvider](s.sc)")
	node.AppendLine("	}")
	node.AppendLine("	return s.scopeProvider")
	node.AppendLine("}")
	node.AppendLine()

	for _, field := range context.fields {
		node.AppendLine("func (s *Default", context.grammar.Name(), "ReferenceLinker) Link", field.typeName, field.name, "(ctx context.Context, reference *core.Reference[", field.target, "]) (*core.SymbolDescription, *core.ReferenceError) {")
		node.AppendLine("    scope := s.scope().Scope", field.typeName, field.name, "(ctx, reference)")
		node.AppendLine("    return core.DefaultLink(scope, reference.Text())")
		node.AppendLine("}").AppendLine()
	}
	return node
}

// topoSortTargets returns uniqueTargets sorted so that child types appear before
// their parent types. This ensures a Go type switch in Put matches the most
// specific case first, so each node lands in exactly one (most-derived) list.
func topoSortTargets(uniqueTargets []string, ifaceParents map[string][]string) []string {
	targetSet := map[string]bool{}
	for _, t := range uniqueTargets {
		targetSet[t] = true
	}
	visited := map[string]bool{}
	result := []string{}
	var visit func(name string)
	visit = func(name string) {
		if visited[name] {
			return
		}
		visited[name] = true
		for _, parent := range ifaceParents[name] {
			visit(parent)
		}
		// Prepend so children end up before parents in the final slice.
		result = append([]string{name}, result...)
	}
	for _, t := range uniqueTargets {
		visit(t)
	}
	// Keep only types that were in uniqueTargets (visit may have walked ancestors
	// not in the set; filter them out while preserving order).
	filtered := result[:0]
	for _, t := range result {
		if targetSet[t] {
			filtered = append(filtered, t)
		}
	}
	return filtered
}

// subtypesInTargets returns all members of uniqueTargets (including t itself)
// that are t or a transitive descendant of t.
func subtypesInTargets(t string, uniqueTargets []string, ifaceParents map[string][]string) []string {
	isDescendantOf := func(candidate, ancestor string) bool {
		visited := map[string]bool{}
		var check func(name string) bool
		check = func(name string) bool {
			if name == ancestor {
				return true
			}
			if visited[name] {
				return false
			}
			visited[name] = true
			return slices.ContainsFunc(ifaceParents[name], check)
		}
		return check(candidate)
	}
	result := []string{}
	for _, u := range uniqueTargets {
		if isDescendantOf(u, t) {
			result = append(result, u)
		}
	}
	return result
}

func generateSymbolContainers(context *LinkerGeneratorContext) generator.Node {
	seen := map[string]bool{}
	uniqueTargets := []string{}
	for _, field := range context.fields {
		if !seen[field.target] {
			seen[field.target] = true
			uniqueTargets = append(uniqueTargets, field.target)
		}
	}
	// Sort alphabetically to make output deterministic; topoSortTargets will reorder as needed.
	sort.Strings(uniqueTargets)
	// Sort so children appear before parents in the Put type switch.
	sortedTargets := topoSortTargets(uniqueTargets, context.ifaceParents)

	name := context.grammar.Name()
	node := generator.NewNode()

	node.AppendLine("type ", name, "SymbolContainers struct{}")
	node.AppendLine()
	node.AppendLine("func (c *", name, "SymbolContainers) New() core.SymbolContainer {")
	node.AppendLine("    return &", name, "SymbolContainer{}")
	node.AppendLine("}")
	node.AppendLine()
	node.AppendLine("func NewSymbolContainers() *", name, "SymbolContainers {")
	node.AppendLine("    return &", name, "SymbolContainers{}")
	node.AppendLine("}")
	node.AppendLine()

	// One slice per unique target type.
	node.AppendLine("type ", name, "SymbolContainer struct {")
	node.Indent(func(n generator.Node) {
		for _, target := range sortedTargets {
			n.AppendLine(target, "s []*core.SymbolDescription")
		}
	})
	node.AppendLine("}")
	node.AppendLine()

	// Put children-first switch so each node lands in exactly one list.
	node.AppendLine("func (sc *", name, "SymbolContainer) Put(desc *core.SymbolDescription) bool {")
	node.Indent(func(n generator.Node) {
		if len(sortedTargets) > 0 {
			n.AppendLine("switch desc.Node.(type) {")
			for _, target := range sortedTargets {
				n.AppendLine("case ", target, ":")
				n.AppendLine("    sc.", target, "s = append(sc.", target, "s, desc)")
				n.AppendLine("    return true")
			}
			n.AppendLine("}")
		}
		n.AppendLine("return false")
	})
	node.AppendLine("}")
	node.AppendLine()

	node.AppendLine("func (sc *", name, "SymbolContainer) All() core.SymbolSeq {")
	if len(sortedTargets) == 0 {
		node.AppendLine("return core.EmptySymbolDescriptions")
	} else {
		node.Indent(func(n generator.Node) {
			n.AppendLine("return extiter.Concat(")
			n.Indent(func(n2 generator.Node) {
				for _, target := range sortedTargets {
					n2.AppendLine("slices.Values(sc.", target, "s),")
				}
			})
			n.AppendLine(")")
		})
	}
	node.AppendLine("}")
	node.AppendLine()

	// Type vars and Type method.
	// For a parent type, Type returns its own list PLUS all descendant lists
	for _, target := range sortedTargets {
		node.AppendLine("var TypeFor_", target, " = reflect.TypeFor[", target, "]()")
	}
	node.AppendLine()
	node.AppendLine("func (sc *", name, "SymbolContainer) ForType(t reflect.Type) core.SymbolSeq {")
	node.Indent(func(n generator.Node) {
		if len(sortedTargets) > 0 {
			n.AppendLine("switch t {")
			for _, target := range sortedTargets {
				subtypes := subtypesInTargets(target, sortedTargets, context.ifaceParents)
				n.AppendLine("case TypeFor_", target, ":")
				if len(subtypes) == 1 {
					// No descendants in target set, simple path.
					n.AppendLine("    return slices.Values(sc.", target, "s)")
				} else {
					// Include this type's list and all descendant lists.
					n.AppendLine("    return extiter.Concat(")
					n.Indent(func(n2 generator.Node) {
						for _, sub := range subtypes {
							n2.AppendLine("slices.Values(sc.", sub, "s),")
						}
					})
					n.AppendLine("    )")
				}
			}
			n.AppendLine("}")
		}

		n.AppendLine("return core.EmptySymbolDescriptions")
	})
	node.AppendLine("}")
	node.AppendLine()

	return node
}

func generateReferenceConstructor(context *LinkerGeneratorContext) generator.Node {
	node := generator.NewNode()
	node.AppendLine("type ", context.grammar.Name(), "ReferencesConstructor interface {")
	node.Indent(func(n generator.Node) {
		for _, field := range context.fields {
			n.AppendLine(field.typeName, field.name, "(owner core.AstNode, unit core.StringUnit) *core.Reference[", field.target, "]")
		}
	})
	node.AppendLine("}")
	node.AppendLine()

	node.AppendLine("type Default", context.grammar.Name(), "ReferencesConstructor struct {")
	node.AppendLine("	sc              *service.Container")
	node.AppendLine("	referenceLinker ", context.grammar.Name(), "ReferenceLinker")
	node.AppendLine("}")
	node.AppendLine()

	node.AppendLine("func NewDefault", context.grammar.Name(), "ReferencesConstructor(sc *service.Container) ", context.grammar.Name(), "ReferencesConstructor {")
	node.AppendLine("	return &Default", context.grammar.Name(), "ReferencesConstructor{sc: sc}")
	node.AppendLine("}")
	node.AppendLine()
	node.AppendLine("func (s *Default", context.grammar.Name(), "ReferencesConstructor) linker() ", context.grammar.Name(), "ReferenceLinker {")
	node.AppendLine("	if s.referenceLinker == nil {")
	node.AppendLine("		s.referenceLinker = service.MustGet[", context.grammar.Name(), "ReferenceLinker](s.sc)")
	node.AppendLine("	}")
	node.AppendLine("	return s.referenceLinker")
	node.AppendLine("}")
	node.AppendLine()

	for _, field := range context.fields {
		node.AppendLine("func (s *Default", context.grammar.Name(), "ReferencesConstructor) ", field.typeName, field.name, "(owner core.AstNode, unit core.StringUnit) *core.Reference[", field.target, "] {")
		node.AppendLine("    fn := s.linker().Link", field.typeName, field.name)
		node.AppendLine("    return core.NewReference(owner, unit, fn)")
		node.AppendLine("}").AppendLine()
	}
	return node
}
