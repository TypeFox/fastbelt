package generator

import (
	"typefox.dev/fastbelt/generator"
	"typefox.dev/fastbelt/internal/grammar"
)

func GenerateServices(grammr grammar.Grammar, packageName string) string {
	node := generator.NewNode()
	node.AppendLine("// Code generated by typefox.dev/fastbelt/cmd. DO NOT EDIT.")
	node.AppendLine()
	node.AppendLine("package ", packageName)
	node.AppendLine()
	node.AppendLine("import (")
	node.Indent(func(n generator.Node) {
		n.AppendLine("\"typefox.dev/fastbelt/workspace\"")
	})
	node.AppendLine(")").AppendLine()

	node.AppendLine("type ", grammr.Name(), "LinkingSrv struct {")
	node.Indent(func(n generator.Node) {
		n.AppendLine("ScopeProvider       ", grammr.Name(), "ScopeProvider")
		n.AppendLine("ReferenceLinker     ", grammr.Name(), "ReferenceLinker")
		n.AppendLine("ReferencesConstructor ", grammr.Name(), "ReferencesConstructor")
	})
	node.AppendLine("}").AppendLine()
	node.AppendLine("type ", grammr.Name(), "LinkingSrvCont interface {")
	node.AppendLine("	workspace.WorkspaceSrvCont")
	node.AppendLine("	", grammr.Name(), "Linking() *", grammr.Name(), "LinkingSrv")
	node.AppendLine("}").AppendLine()

	node.AppendLine("type ", grammr.Name(), "LinkingSrvContBlock struct {")
	node.AppendLine("	", "fastbeltLinking ", grammr.Name(), "LinkingSrv")
	node.AppendLine("}").AppendLine()
	node.AppendLine("func (b *", grammr.Name(), "LinkingSrvContBlock) ", grammr.Name(), "Linking() *", grammr.Name(), "LinkingSrv {")
	node.AppendLine("	return &b.fastbeltLinking")
	node.AppendLine("}").AppendLine()

	node.AppendLine("type ", grammr.Name(), "GeneratedSrvCont interface {")
	node.Indent(func(n generator.Node) {
		n.AppendLine("workspace.GeneratedSrvCont")
		n.AppendLine(grammr.Name(), "LinkingSrvCont")
	})
	node.AppendLine("}").AppendLine()

	node.AppendLine("func CreateDefaultServices(srv ", grammr.Name(), "GeneratedSrvCont) {")
	node.Indent(func(n generator.Node) {
		n.AppendLine("linking := srv.", grammr.Name(), "Linking()")
		n.AppendLine("if linking.ScopeProvider == nil {")
		n.AppendLine("    linking.ScopeProvider = NewDefault", grammr.Name(), "ScopeProvider(srv)")
		n.AppendLine("}")
		n.AppendLine("if linking.ReferenceLinker == nil {")
		n.AppendLine("    linking.ReferenceLinker = NewDefault", grammr.Name(), "ReferenceLinker(srv)")
		n.AppendLine("}")
		n.AppendLine("if linking.ReferencesConstructor == nil {")
		n.AppendLine("    linking.ReferencesConstructor = NewDefault", grammr.Name(), "ReferencesConstructor(srv)")
		n.AppendLine("}")
		n.AppendLine("generated := srv.Generated()")
		n.AppendLine("if generated.Lexer == nil {")
		n.Indent(func(n2 generator.Node) {
			n2.AppendLine("generated.Lexer = NewLexer()")
		})
		n.AppendLine("}")
		n.AppendLine("if generated.Parser == nil {")
		n.Indent(func(n2 generator.Node) {
			n2.AppendLine("generated.Parser = NewParser(srv)")
		})
		n.AppendLine("}")
	})
	node.AppendLine("}")
	node.AppendLine()
	return FormatIfPossible(node.String())
}
