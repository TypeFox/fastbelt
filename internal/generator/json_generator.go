package generator

import (
	"strings"

	"typefox.dev/fastbelt/internal/grammar"
	"typefox.dev/fastbelt/util/codegen"
)

func GenerateJSON(grammr grammar.Grammar, packageName string) string {
	node := codegen.NewNode()
	node.AppendLine("// Code generated by typefox.dev/fastbelt/cmd/fastbelt. DO NOT EDIT.")
	node.AppendLine()
	node.AppendLine("package ", packageName)
	node.AppendLine()

	node.AppendLine("import (")
	node.Indent(func(n codegen.Node) {
		n.AppendLine("\"encoding/json\"")
		n.AppendLine()
		n.AppendLine("core \"typefox.dev/fastbelt\"")
	})
	node.AppendLine(")")
	node.AppendLine()

	for _, iface := range grammr.Interfaces() {
		generateJSONMarshal(node, iface)
	}

	for _, iface := range grammr.Interfaces() {
		generateJSONUnmarshal(node, iface)
	}

	return FormatIfPossible(node.String())
}

func generateJSONMarshal(node codegen.Node, iface grammar.Interface) {
	fields := []FieldInfo{}
	for _, field := range iface.Fields() {
		fields = append(fields, getFieldInfo(field))
	}

	node.AppendLine("func (i *", iface.Name(), "Data) MarshalJSON() ([]byte, error) {")
	node.Indent(func(n codegen.Node) {
		n.AppendLine("return json.Marshal(struct {")
		n.Indent(func(n2 codegen.Node) {
			n2.AppendLine("T__", " ", "string", " `json:\"$type\"`")
			for _, field := range fields {
				jsonTag := strings.ToLower(field.Name[:1]) + field.Name[1:]
				var typeStr string
				if field.Array {
					typeStr = "[]" + field.GType
				} else {
					typeStr = field.Type
				}
				n2.AppendLine(field.Name, " ", typeStr, " `json:\"", jsonTag, "\"`")
			}
		})
		n.AppendLine("}{")
		n.Indent(func(n2 codegen.Node) {
			n2.AppendLine("T__: ", "\"", iface.Name(), "\",")
			for _, field := range fields {
				getterName := field.Name
				if field.Boolean && !field.Array {
					getterName = "Is" + field.Name
				}
				n2.AppendLine(field.Name, ": i.", getterName, "(),")
			}
		})
		n.AppendLine("})")
	})
	node.AppendLine("}")
	node.AppendLine()
}

func getAuxFieldType(field FieldInfo) string {
	if field.Reference {
		if field.Array {
			return "[]" + field.GType
		}
		return field.Type
	}
	if field.Array {
		if field.Boolean {
			return "[]bool"
		}
		if field.GType == TOKEN_TYPE || field.GType == COMPOSITE_TYPE {
			return "[]string"
		}
		return "[]*" + field.GType + "Impl"
	}
	if field.Boolean {
		return "bool"
	}
	if field.GType == TOKEN_TYPE || field.GType == COMPOSITE_TYPE {
		return "string"
	}
	return "*" + field.GType + "Impl"
}

func generateJSONUnmarshal(node codegen.Node, iface grammar.Interface) {
	fields := []FieldInfo{}
	for _, field := range iface.Fields() {
		fields = append(fields, getFieldInfo(field))
	}

	node.AppendLine("func (i *", iface.Name(), "Data) UnmarshalJSON(data []byte) error {")
	node.Indent(func(n codegen.Node) {
		n.AppendLine("aux := &struct {")
		n.Indent(func(n2 codegen.Node) {
			n2.AppendLine("T__", " ", "string", " `json:\"$type\"`")
			for _, field := range fields {
				jsonTag := strings.ToLower(field.Name[:1]) + field.Name[1:]
				n2.AppendLine(field.Name, " ", getAuxFieldType(field), " `json:\"", jsonTag, "\"`")
			}
		})
		n.AppendLine("}{}")
		n.AppendLine("if err := json.Unmarshal(data, aux); err != nil {")
		n.Indent(func(n2 codegen.Node) {
			n2.AppendLine("return err")
		})
		n.AppendLine("}")

		hasComposeNodeTempVar := false
		for _, field := range fields {
			if field.Array {
				n.AppendLine("i.", field.PName, " = []", field.GType, "{}")
				n.AppendLine("for _, item := range aux.", field.Name, " {")
				n.Indent(func(n2 codegen.Node) {
					n2.AppendLine("i.Set", field.Name, "Item(item)")
				})
				n.AppendLine("}")
			} else if field.Boolean || field.HasTokenGetter {
				n.AppendLine("i.Set", field.Name, "(&core.Token{Image: aux.", field.Name, "})")
			} else if field.HasNodeGetter {
				if !hasComposeNodeTempVar {
					n.AppendLine("var cn core.CompositeNode")
				}
				n.AppendLine("cn = core.NewCompositeNode()")
				n.AppendLine("cn.SetToken(&core.Token{Image: aux.", field.Name, "})")
				n.AppendLine("i.Set", field.Name, "(cn)")
			} else {
				n.AppendLine("i.Set", field.Name, "(aux.", field.Name, ")")
			}
		}
		n.AppendLine("return nil")
	})
	node.AppendLine("}")
	node.AppendLine()
}
