type ParserGeneratorContext struct {
	grammar            grammar.Grammar
	accessNamesCounter map[string]int
	accessNames        map[core.AstNode]string
	lookaheads         map[core.AstNode]LookaheadValue
	orLookaheads       map[core.AstNode]LookaheadValue
	inCompositeRule    bool
	// ATN-derived maps for error recovery emit (nil when ATN not built).
	followStateIdx   map[core.AstNode]int // grammar.RuleCall: follow-state array index
	decisionStateIdx map[core.AstNode]int // grammar.Element: decision-state array index
	// completion is true when this context is producing completion_parser_gen.go
	// rather than parser_gen.go. In that mode, AST-construction emits are
	// suppressed and CompletionParserState bookkeeping (EnterRule, RecordSnapshot,
	// MarkAssignment) is inserted instead. See GenerateCompletionParser.
	completion bool
	// ruleStartIdx maps each rule (AbstractRuleWithBody as core.AstNode) to its
	// ATN RuleStart state array index. Used in completion mode for
	// p.cp.EnterRule(ruleKey, ruleStartStateIdx).
	ruleStartIdx map[core.AstNode]int
}

type LookaheadValue struct {
	name string
	llk  LLkLookahead
}

func (context *ParserGeneratorContext) SetAccessName(node core.AstNode, name string) {
	index := context.accessNamesCounter[name]
	context.accessNames[node] = name + "_" + strconv.Itoa(index)
	index++
	context.accessNamesCounter[name] = index
}

func GenerateParser(grammr grammar.Grammar, packageName string, tokenTypes GenerateTokenTypesResult) string {
	context := &ParserGeneratorContext{
		grammar:            grammr,
		accessNamesCounter: make(map[string]int),
		accessNames:        make(map[core.AstNode]string),
		lookaheads:         make(map[core.AstNode]LookaheadValue),
		orLookaheads:       make(map[core.AstNode]LookaheadValue),
	}

	// Build the ATN and derive index maps for EnterRule/Sync emit.
	builtATN, _ := internalATN.CreateATN(grammr, tokenTypes.TokenTypeIds)
	if builtATN != nil {
		stateIdx := internalATN.BuildStateIndexMap(builtATN)
		context.followStateIdx = buildFollowStateIdxMap(builtATN, stateIdx)
		context.decisionStateIdx = buildDecisionStateIdxMap(builtATN, stateIdx)
	}

	populateContext(context)
	node := codegen.NewNode()
	node.AppendLine("// Code generated by typefox.dev/fastbelt/cmd/fastbelt. DO NOT EDIT.")
	node.AppendLine()
	node.AppendLine("package ", packageName)
	node.AppendLine()
	node.AppendLine("import (")
	node.Indent(func(n codegen.Node) {
		n.AppendLine("\"sync\"")
		n.AppendLine("core \"typefox.dev/fastbelt\"")
		n.AppendLine("\"typefox.dev/fastbelt/parser\"")
		n.AppendLine("\"typefox.dev/fastbelt/util/service\"")
	})
	node.AppendLine(")")
	node.AppendLine()

	node.AppendLine("type Parser struct {")
	node.Indent(func(n codegen.Node) {
		n.AppendLine("state *parser.ParserState")
		n.AppendLine("sc *service.Container")
		n.AppendLine("referencesConstructor ", grammr.Name(), "ReferencesConstructor")
		n.AppendLine("atn func() *parser.RuntimeATN")
	})
	node.AppendLine("}")
	node.AppendLine()

	rules := grammr.Rules()
	if len(rules) == 0 {
		return FormatIfPossible(node.String())
	}

	firstRule := rules[0]
	node.AppendLine("func (p *Parser) Parse(document *core.Document) *parser.ParseResult {")
	node.Indent(func(n codegen.Node) {
		n.AppendLine("recovery := service.MustGet[parser.ErrorRecoveryStrategy](p.sc)")
		n.AppendLine("messages := service.MustGet[parser.ErrorMessageProvider](p.sc)")
		n.AppendLine("referencesConstructor := service.MustGet[", grammr.Name(), "ReferencesConstructor](p.sc)")
		n.AppendLine("cp := &Parser{sc: p.sc, referencesConstructor: referencesConstructor, atn: p.atn, state: parser.NewParserState(document.Tokens, p.atn(), recovery, messages)}")
		n.AppendLine("result := cp.Parse", firstRule.Name(), "()")
		n.AppendLine("core.AssignContainers(document, result)")
		n.AppendLine("return &parser.ParseResult{Node: result, Errors: cp.state.Errors()}")
	})
	node.AppendLine("}")
	node.AppendLine()
	node.AppendLine("func NewParser(sc *service.Container) *Parser {")
	node.Indent(func(n codegen.Node) {
		n.AppendLine("return &Parser{")
		n.AppendLine("	sc: sc,")
		n.AppendLine("	atn: sync.OnceValue(BuildATN),")
		n.AppendLine("}")
	})
	node.AppendLine("}")
	node.AppendLine()

	node.AppendLine("const (")
	accessIota := true
	accessNames := make([]string, 0, len(context.accessNames))
	node.Indent(func(access codegen.Node) {
		for _, name := range context.accessNames {
			accessNames = append(accessNames, name)
		}
		slices.Sort(accessNames)
		for _, name := range accessNames {
			if accessIota {
				access.AppendLine(name + " = iota + 1")
				accessIota = false
			} else {
				access.AppendLine(name)
			}
		}
	})
	node.AppendLine(")")
	node.AppendLine()

	lookaheads := make([]LookaheadValue, 0, len(context.lookaheads))

	for _, lookahead := range context.lookaheads {
		lookaheads = append(lookaheads, lookahead)
	}
	for _, lookahead := range context.orLookaheads {
		lookaheads = append(lookaheads, lookahead)
	}
	slices.SortFunc(lookaheads, func(a, b LookaheadValue) int {
		return strings.Compare(a.name, b.name)
	})
	for _, lookahead := range lookaheads {
		generateLLkLookahead(node, lookahead.name, lookahead.llk)
		node.AppendLine()
	}

	for _, rule := range grammr.Rules() {
		generateParseFunction(node, context, rule)
	}
	for _, composite := range grammr.Composites() {
		generateCompositeParseFunction(node, context, composite)
	}

	return FormatIfPossible(node.String())
}

// GenerateCompletionParser emits completion_parser_gen.go — a peer of
// parser_gen.go that mirrors the main parser's control flow but skips every
// AST-mutation call and instead records the CompletionParserState bookkeeping
// (rule stack, ATN snapshots, assignment markers) the completion provider
// needs.
//
// The generated file reuses the lookahead constants, keyword constants, and
// ATN-builder function defined by GenerateParser/EmitGoSource, so it must be
// emitted into the same package.
func GenerateCompletionParser(grammr grammar.Grammar, packageName string, tokenTypes GenerateTokenTypesResult) string {
	context := &ParserGeneratorContext{
		grammar:            grammr,
		accessNamesCounter: make(map[string]int),
		accessNames:        make(map[core.AstNode]string),
		lookaheads:         make(map[core.AstNode]LookaheadValue),
		orLookaheads:       make(map[core.AstNode]LookaheadValue),
		completion:         true,
	}

	builtATN, _ := internalATN.CreateATN(grammr, tokenTypes.TokenTypeIds)
	if builtATN != nil {
		stateIdx := internalATN.BuildStateIndexMap(builtATN)
		context.followStateIdx = buildFollowStateIdxMap(builtATN, stateIdx)
		context.decisionStateIdx = buildDecisionStateIdxMap(builtATN, stateIdx)
		context.ruleStartIdx = buildRuleStartIdxMap(builtATN, stateIdx)
	}

	populateContext(context)
	node := codegen.NewNode()
	node.AppendLine("// Code generated by typefox.dev/fastbelt/cmd/fastbelt. DO NOT EDIT.")
	node.AppendLine()
	node.AppendLine("package ", packageName)
	node.AppendLine()
	node.AppendLine("import (")
	node.Indent(func(n codegen.Node) {
		n.AppendLine("\"sync\"")
		n.AppendLine("core \"typefox.dev/fastbelt\"")
		n.AppendLine("\"typefox.dev/fastbelt/parser\"")
		n.AppendLine("\"typefox.dev/fastbelt/util/service\"")
	})
	node.AppendLine(")")
	node.AppendLine()

	node.AppendLine("type CompletionParser struct {")
	node.Indent(func(n codegen.Node) {
		n.AppendLine("state *parser.ParserState")
		n.AppendLine("cp *parser.CompletionParserState")
		n.AppendLine("sc *service.Container")
		n.AppendLine("atn func() *parser.RuntimeATN")
	})
	node.AppendLine("}")
	node.AppendLine()

	rules := grammr.Rules()
	if len(rules) == 0 {
		return FormatIfPossible(node.String())
	}
	firstRule := rules[0]

	node.AppendLine("func NewCompletionParser(sc *service.Container) *CompletionParser {")
	node.Indent(func(n codegen.Node) {
		n.AppendLine("return &CompletionParser{")
		n.AppendLine("	sc: sc,")
		n.AppendLine("	atn: sync.OnceValue(BuildATN),")
		n.AppendLine("}")
	})
	node.AppendLine("}")
	node.AppendLine()

	node.AppendLine("// Parse runs the completion parser over the given prefix tokens (typically")
	node.AppendLine("// the document's tokens up to the cursor) and returns the recorded")
	node.AppendLine("// snapshots and rule stack. The completion provider feeds that result into")
	node.AppendLine("// the ATN simulator.")
	node.AppendLine("func (p *CompletionParser) Parse(tokens []core.Token) *parser.CompletionParseResult {")
	node.Indent(func(n codegen.Node) {
		n.AppendLine("messages := service.MustGet[parser.ErrorMessageProvider](p.sc)")
		n.AppendLine("recovery := service.MustGet[parser.ErrorRecoveryStrategy](p.sc)")
		n.AppendLine("cp := &CompletionParser{sc: p.sc, atn: p.atn}")
		n.AppendLine("cp.state = parser.NewParserState(tokens, p.atn(), recovery, messages)")
		n.AppendLine("cp.cp = parser.NewCompletionParserState(cp.state)")
		n.AppendLine("cp.Parse", firstRule.Name(), "()")
		n.AppendLine("return cp.cp.Result(tokens)")
	})
	node.AppendLine("}")
	node.AppendLine()

	for _, rule := range grammr.Rules() {
		generateParseFunction(node, context, rule)
	}
	for _, composite := range grammr.Composites() {
		generateCompositeParseFunction(node, context, composite)
	}

	return FormatIfPossible(node.String())
}

// buildFollowStateIdxMap returns a map from grammar.RuleCall (as core.AstNode) to
// the array index of the ATN follow state for that rule call.
func buildFollowStateIdxMap(builtATN *internalATN.ATN, stateIdx map[*internalATN.ATNState]int) map[core.AstNode]int {
	result := make(map[core.AstNode]int)
	for _, state := range builtATN.States {
		if state.RuleCallEntry == nil {
			continue
		}
		for _, t := range state.Transitions {
			if rt, ok := t.(*internalATN.RuleTransition); ok {
				result[state.RuleCallEntry] = stateIdx[rt.FollowState]
				break
			}
		}
	}
	return result
}

// buildDecisionStateIdxMap returns a map from grammar.Element (as core.AstNode) to
// the array index of the ATN decision state for that element.
func buildDecisionStateIdxMap(builtATN *internalATN.ATN, stateIdx map[*internalATN.ATNState]int) map[core.AstNode]int {
	result := make(map[core.AstNode]int, len(builtATN.DecisionMap))
	for _, state := range builtATN.DecisionMap {
		if state.Production != nil {
			result[state.Production] = stateIdx[state]
		}
	}
	return result
}

// buildRuleStartIdxMap returns a map from each parser/composite rule (as
// core.AstNode) to the array index of its ATN RuleStart state. The completion
// parser uses this to record the "we just entered rule X" snapshot.
func buildRuleStartIdxMap(builtATN *internalATN.ATN, stateIdx map[*internalATN.ATNState]int) map[core.AstNode]int {
	result := make(map[core.AstNode]int, len(builtATN.RuleToStartState))
	for rule, state := range builtATN.RuleToStartState {
		result[rule] = stateIdx[state]
	}
	return result
}

// buildSyncCall returns the Go statement to emit for a Sync call at the given
// element's decision state, or "" if no decision state is known.
//
// In completion mode the emitted statement also records an ATN snapshot at the
// same state index, so the completion provider has a starting point for the
// simulator at every branch boundary.
func buildSyncCall(context *ParserGeneratorContext, el core.AstNode) string {
	if context.decisionStateIdx == nil {
		return ""
	}
	idx, ok := context.decisionStateIdx[el]
	if !ok {
		return ""
	}
	idxStr := strconv.Itoa(idx)
	if context.completion {
		return "p.cp.RecordSnapshot(" + idxStr + "); p.state.Sync(" + idxStr + ")"
	}
	return "p.state.Sync(" + idxStr + ")"
}

func populateContext(context *ParserGeneratorContext) {
	for _, rule := range context.grammar.Rules() {
		ruleName := rule.Name()
		populateContextWithNode(context, ruleName, rule.Body())
	}
	for _, composite := range context.grammar.Composites() {
		ruleName := composite.Name()
		populateContextWithNode(context, ruleName, composite.Body())
	}
}

// TODO: use the prefix with first letter lowercase for global constants and variables to keep them private
func populateContextWithNode(context *ParserGeneratorContext, prefix string, node core.AstNode) {
	if _, exists := context.accessNames[node]; exists {
		return
	}
	switch n := node.(type) {
	case grammar.Alternatives:
		if len(n.Alts()) > 1 {
			name := prefix + "LookaheadOr" + strconv.Itoa(len(context.orLookaheads))
			context.orLookaheads[n] = LookaheadValue{name: name, llk: GetLLkLookaheadOr(context.grammar, n)}
		}
		if n.Cardinality() != CardinalityOne {
			name := prefix + "Lookahead" + strconv.Itoa(len(context.lookaheads))
			context.lookaheads[n] = LookaheadValue{name: name, llk: GetLLkLookaheadOpt(context.grammar, n)}
		}
		for _, alt := range n.Alts() {
			populateContextWithNode(context, prefix, alt)
		}
	case grammar.Group:
		if n.Cardinality() != CardinalityOne {
			name := prefix + "Lookahead" + strconv.Itoa(len(context.lookaheads))
			context.lookaheads[n] = LookaheadValue{name: name, llk: GetLLkLookaheadOpt(context.grammar, n)}
		}
		for _, element := range n.Elements() {
			populateContextWithNode(context, prefix, element)
		}
	case grammar.Keyword:
		if n.Cardinality() != CardinalityOne {
			name := prefix + "Lookahead" + strconv.Itoa(len(context.lookaheads))
			context.lookaheads[n] = LookaheadValue{name: name, llk: GetLLkLookaheadOpt(context.grammar, n)}
		}
		name := prefix + KeywordName(n)
		context.SetAccessName(node, name)
	case grammar.Assignment:
		if n.Cardinality() != CardinalityOne {
			name := prefix + "Lookahead" + strconv.Itoa(len(context.lookaheads))
			context.lookaheads[n] = LookaheadValue{name: name, llk: GetLLkLookaheadOpt(context.grammar, n)}
		}
		name := prefix + n.Property().Text()
		populateContextWithNode(context, name, n.Value())
	case grammar.CrossRef:
		populateContextWithNode(context, prefix, n.Rule())
	case grammar.RuleCall:
		if n.Cardinality() != CardinalityOne {
			name := prefix + "Lookahead" + strconv.Itoa(len(context.lookaheads))
			context.lookaheads[n] = LookaheadValue{name: name, llk: GetLLkLookaheadOpt(context.grammar, n)}
		}
		ruleRef := n.Rule().Ref(ctx.Background())
		if token, ok := ruleRef.(grammar.Token); ok {
			name := prefix + token.Name()
			context.SetAccessName(node, name)
		}
	}
}

func generateParseFunction(node codegen.Node, context *ParserGeneratorContext, rule grammar.ParserRule) {
	returnType := grammar.FindReturnType(rule, ctx.Background())
	if returnType == nil {
		panic("Unable to find return type for rule: " + rule.Name())
	}
	context.inCompositeRule = false
	receiverType := context.receiverType()
	if context.completion {
		node.AppendLine("func (p *", receiverType, ") Parse", rule.Name(), "() {")
		node.Indent(func(n codegen.Node) {
			ruleStartIdx := 0
			if context.ruleStartIdx != nil {
				if idx, ok := context.ruleStartIdx[rule]; ok {
					ruleStartIdx = idx
				}
			}
			n.AppendLine("p.cp.EnterRule(", strconv.Quote(rule.Name()), ", ", strconv.Itoa(ruleStartIdx), ")")
			n.AppendLine("defer p.cp.ExitRule()")
			generateAbstractElementParser(n, context, rule.Body())
		})
		node.AppendLine("}")
		node.AppendLine()
		return
	}
	node.AppendLine("func (p *", receiverType, ") Parse", rule.Name(), "() ", returnType.Name(), " {")
	node.Indent(func(n codegen.Node) {
		n.AppendLine("current := New", returnType.Name(), "()")
		n.AppendLine("current.SetSegmentStartToken(p.state.LARaw(1))")
		generateAbstractElementParser(n, context, rule.Body())
		n.AppendLine("current.SetSegmentEndToken(p.state.LARaw(0))")
		n.AppendLine("return current")
	})
	node.AppendLine("}")
	node.AppendLine()
}

func generateCompositeParseFunction(node codegen.Node, context *ParserGeneratorContext, rule grammar.CompositeRule) {
	context.inCompositeRule = true
	receiverType := context.receiverType()
	if context.completion {
		node.AppendLine("func (p *", receiverType, ") Parse", rule.Name(), "() {")
		node.Indent(func(n codegen.Node) {
			ruleStartIdx := 0
			if context.ruleStartIdx != nil {
				if idx, ok := context.ruleStartIdx[rule]; ok {
					ruleStartIdx = idx
				}
			}
			n.AppendLine("p.cp.EnterRule(", strconv.Quote(rule.Name()), ", ", strconv.Itoa(ruleStartIdx), ")")
			n.AppendLine("defer p.cp.ExitRule()")
			generateAbstractElementParser(n, context, rule.Body())
		})
		node.AppendLine("}")
		node.AppendLine()
		return
	}
	node.AppendLine("func (p *", receiverType, ") Parse", rule.Name(), "(current core.CompositeNode) {")
	node.Indent(func(n codegen.Node) {
		generateAbstractElementParser(n, context, rule.Body())
	})
	node.AppendLine("}")
	node.AppendLine()
}

// receiverType is the Go struct name that owns the generated Parse methods.
// In the standard generator this is "Parser"; in completion mode it is
// "CompletionParser".
func (c *ParserGeneratorContext) receiverType() string {
	if c.completion {
		return "CompletionParser"
	}
	return "Parser"
}

func generateAbstractElementParser(node codegen.Node, context *ParserGeneratorContext, element grammar.Element) {
	switch e := element.(type) {
	case grammar.Alternatives:
		generateAlternativesParser(node, context, e)
	case grammar.Group:
		generateGroupParser(node, context, e)
	case grammar.Action:
		if context.completion {
			// Actions only mutate AST structure (re-anchoring current); they
			// consume no tokens and have no ATN impact, so completion parser
			// emits nothing for them.
			return
		}
		node.AppendLine("{")
		node.Indent(func(n codegen.Node) {
			n.AppendLine("result := New", e.Type().Text(), "()")
			// Inherit segment from previous node
			n.AppendLine("result.SetSegment(current.Segment())")
			if e.Property() != nil {
				if e.Operator() == "+=" {
					n.AppendLine("result.Set", e.Property().Text(), "Item(current)")
				} else {
					n.AppendLine("result.Set", e.Property().Text(), "(current)")
				}
				// Ensure that the previous node has a valid segment ending
				n.AppendLine("current.SetSegmentEndToken(p.state.LARaw(0))")
				n.AppendLine("current = result")
			} else {
				// If there is no property to assign, just merge tokens
				n.AppendLine("core.AssignTokens(result, current.Tokens())")
				n.AppendLine("current = result")
			}
		})
		node.AppendLine("}")
		node.AppendLine("current := current.(", e.Type().Text(), ")")
	case grammar.Keyword:
		node.AppendLine("{")
		node.Indent(func(indent codegen.Node) {
			generateKeywordParser(indent, context, e)
		})
		node.AppendLine("}")
	case grammar.RuleCall:
		node.AppendLine("{")
		node.Indent(func(indent codegen.Node) {
			resultName := generateRuleCallParser(indent, context, e)
			if context.completion {
				return
			}
			if resultName == "result" && !context.inCompositeRule {
				// Unassigned rule call result
				// Needs to be merged into current node
				indent.AppendLine("core.MergeTokens(result, current.Tokens())")
				indent.AppendLine("current = result")
			}
		})
		node.AppendLine("}")
	case grammar.Assignment:
		node.AppendLine("{")
		node.Indent(func(indent codegen.Node) {
			// Decision states in the ATN are keyed by the assignable value
			// (RuleCall, Keyword, etc.), not by the Assignment wrapper itself.
			syncCall := buildSyncCall(context, e.Value())
			var preGuard codegen.Callback
			if syncCall != "" {
				preGuard = func(n codegen.Node) { n.AppendLine(syncCall) }
			}
			generateCardinality(indent, func(n codegen.Node) {
				if context.completion {
					n.AppendLine("p.cp.MarkAssignment(", strconv.Quote(e.Property().Text()), ")")
				}
				generateAssignable(n, context, e.Value(), func(n2 codegen.Node, resultName string) {
					if context.completion {
						// Completion parser doesn't construct the AST, so there
						// is nothing to set on a parent node. The MarkAssignment
						// above is what completion needs from this assignment.
						return
					}
					n2.AppendLine("if ", resultName, " != nil {")
					if _, ok := e.Value().(grammar.CrossRef); ok {
						parserRuleName := getInterfaceName(e)
						// For cross-references, we need to create a Reference object
						resultName = "p.referencesConstructor." + parserRuleName + e.Property().Text() + "(current, " + resultName + ")"
					}
					n2.Indent(func(in codegen.Node) {
						switch e.Operator() {
						case "+=":
							// Append to slice
							in.AppendLine("current.Set", e.Property().Text(), "Item(", resultName, ")")
						default:
							// Single assignment
							in.AppendLine("current.Set", e.Property().Text(), "(", resultName, ")")
						}
					})
					n2.AppendLine("}")
				})
				if context.completion {
					n.AppendLine("p.cp.ClearAssignment()")
				}
			}, func(n codegen.Node) {
				lookaheadName := context.lookaheads[element].name
				lookahead := generateLookaheadString(lookaheadName)
				n.Append(lookahead)
			}, preGuard, element.Cardinality())
		})
		node.AppendLine("}")
	}
}

func generateAssignable(node codegen.Node, context *ParserGeneratorContext, assignable grammar.Assignable, cb func(node codegen.Node, resultName string)) {
	switch a := assignable.(type) {
	case grammar.CrossRef:
		cb(node, generateCrossReferenceParser(node, context, a))
	case grammar.Keyword:
		cb(node, generateKeywordParser(node, context, a))
	case grammar.RuleCall:
		cb(node, generateRuleCallParser(node, context, a))
	case grammar.Alternatives:
		generateAssignableAlternatives(node, context, a, cb)
	default:
		panic("Unresolved assignment assignable")
	}
}

func generateAssignableAlternatives(node codegen.Node, context *ParserGeneratorContext, alts grammar.Alternatives, cb func(node codegen.Node, resultName string)) {
	lookaheadName := context.orLookaheads[alts].name
	node.AppendLine("switch p.state.Lookahead(", lookaheadName, ") {")
	for i, alt := range alts.Alts() {
		node.AppendLine("case ", strconv.Itoa(i), ":")
		node.Indent(func(in codegen.Node) {
			if assignable, ok := alt.(grammar.Assignable); ok {
				generateAssignable(in, context, assignable, cb)
			}
		})
	}
	node.AppendLine("}")
}

func generateCrossReferenceParser(node codegen.Node, context *ParserGeneratorContext, crossRef grammar.CrossRef) string {
	return generateRuleCallParser(node, context, crossRef.Rule())
}

func generateGroupParser(node codegen.Node, context *ParserGeneratorContext, group grammar.Group) {
	lookaheadName := context.lookaheads[group].name
	lookahead := generateLookaheadString(lookaheadName)
	syncCall := buildSyncCall(context, group)
	var preGuard codegen.Callback
	if syncCall != "" {
		preGuard = func(n codegen.Node) { n.AppendLine(syncCall) }
	}
	generateCardinality(node, func(n codegen.Node) {
		for _, element := range group.Elements() {
			generateAbstractElementParser(n, context, element)
		}
	}, func(n codegen.Node) {
		n.Append(lookahead)
	}, preGuard, group.Cardinality())
}

func generateKeywordParser(node codegen.Node, context *ParserGeneratorContext, keyword grammar.Keyword) string {
	lookahead := "p.state.LAId(1) == " + GeneratedKeywordIdxName(keyword)
	generateCardinality(node, func(n codegen.Node) {
		n.AppendLine("token := p.state.Consume(", GeneratedKeywordName(keyword), ")")
		if context.completion {
			// Suppress unused-variable error; the keyword's only purpose in
			// completion is the Consume's side effect on parser state.
			n.AppendLine("_ = token")
			return
		}
		n.AppendLine("core.AssignToken(current, token, ", context.accessNames[keyword], ")")
	}, func(n codegen.Node) { n.Append(lookahead) }, nil, keyword.Cardinality())
	return "token"
}

func generateRuleCallParser(node codegen.Node, context *ParserGeneratorContext, ruleCall grammar.RuleCall) string {
	target := ruleCall.Rule().Ref(ctx.Background())
	lookaheadName := context.lookaheads[ruleCall].name
	lookahead := generateLookaheadString(lookaheadName)
	var result string
	switch target.(type) {
	case grammar.Token:
		result = "token"
	default:
		result = "result"
	}
	first := true
	generateCardinality(node, func(n codegen.Node) {
		eq := "="
		if first {
			eq = ":="
			first = false
		}
		switch t := target.(type) {
		case grammar.Token:
			n.AppendLine("token ", eq, " p.state.Consume(", GeneratedTokenName(t), ")")
			if context.completion {
				n.AppendLine("_ = token")
				return
			}
			n.AppendLine("core.AssignToken(current, token, ", context.accessNames[ruleCall], ")")
		case grammar.ParserRule:
			followIdx := 0
			if context.followStateIdx != nil {
				if idx, ok := context.followStateIdx[ruleCall]; ok {
					followIdx = idx
				}
			}
			n.AppendLine("p.state.EnterRule(", strconv.Itoa(followIdx), ")")
			if context.completion {
				n.AppendLine("p.Parse", t.Name(), "()")
			} else {
				n.AppendLine("result ", eq, " p.Parse", t.Name(), "()")
			}
			n.AppendLine("p.state.ExitRule()")
		case grammar.CompositeRule:
			followIdx := 0
			if context.followStateIdx != nil {
				if idx, ok := context.followStateIdx[ruleCall]; ok {
					followIdx = idx
				}
			}
			if context.completion {
				// Composite rules in completion mode never receive a `current`
				// argument; the generated CompletionParser composite Parse
				// function has the same no-arg shape as a regular rule.
				n.AppendLine("p.state.EnterRule(", strconv.Itoa(followIdx), ")")
				n.AppendLine("p.Parse", t.Name(), "()")
				n.AppendLine("p.state.ExitRule()")
				return
			}
			if context.inCompositeRule {
				n.AppendLine("p.state.EnterRule(", strconv.Itoa(followIdx), ")")
				n.AppendLine("p.Parse", t.Name(), "(current)")
				n.AppendLine("p.state.ExitRule()")
			} else {
				n.AppendLine("result ", eq, " core.NewCompositeNode()")
				n.AppendLine("result.SetSegmentStartToken(p.state.LARaw(1))")
				n.AppendLine("p.state.EnterRule(", strconv.Itoa(followIdx), ")")
				n.AppendLine("p.Parse", t.Name(), "(result)")
				n.AppendLine("p.state.ExitRule()")
				n.AppendLine("result.SetSegmentEndToken(p.state.LARaw(0))")
			}
		}
	}, func(n codegen.Node) { n.Append(lookahead) }, nil, ruleCall.Cardinality())
	return result
}

func generateLookaheadString(name string) string {
	return "p.state.Lookahead(" + name + ") == 0"
}

func generateAlternativesParser(node codegen.Node, context *ParserGeneratorContext, alts grammar.Alternatives) {
	allLookaheadName := context.lookaheads[alts].name
	allLookahead := generateLookaheadString(allLookaheadName)
	lookaheadName := context.orLookaheads[alts].name
	syncCall := buildSyncCall(context, alts)
	var preGuard codegen.Callback
	if syncCall != "" {
		preGuard = func(n codegen.Node) { n.AppendLine(syncCall) }
	}
	generateCardinality(node, func(n codegen.Node) {
		n.AppendLine("switch p.state.Lookahead(", lookaheadName, ") {")
		for i, alt := range alts.Alts() {
			n.AppendLine("case ", strconv.Itoa(i), ":")
			n.Indent(func(in codegen.Node) {
				generateAbstractElementParser(in, context, alt)
			})
		}
		n.AppendLine("default:")
		n.Indent(func(in codegen.Node) {
			in.AppendLine("token := p.state.LA(1)")
			in.AppendLine("p.state.AppendError(p.state.Messages().NoViableAlternative(token), token)")
		})
		n.AppendLine("}")
	}, func(n codegen.Node) {
		n.Append(allLookahead)
	}, preGuard, alts.Cardinality())
}

func generateCardinality(node codegen.Node, element, lookahead codegen.Callback, preGuard codegen.Callback, cardinality string) {
	switch cardinality {
	case CardinalityOne:
		element(node)
	case CardinalityOptional:
		if preGuard != nil {
			preGuard(node)
		}
		node.Append("if ")
		lookahead(node)
		node.AppendLine(" {")
		node.Indent(element)
		node.AppendLine("}")
	case CardinalityZeroOrMore:
		if preGuard != nil {
			preGuard(node)
		}
		node.Append("for ")
		lookahead(node)
		node.AppendLine(" {")
		node.Indent(func(n codegen.Node) {
			element(n)
			if preGuard != nil {
				preGuard(n)
			}
		})
		node.AppendLine("}")
	case CardinalityOneOrMore:
		node.Append("for ok := true; ok; ok = ")
		lookahead(node)
		node.AppendLine(" {")
		node.Indent(func(n codegen.Node) {
			element(n)
			if preGuard != nil {
				preGuard(n)
			}
		})
		node.AppendLine("}")
	}
}

type PartialPathAndSuffixes struct {
	PartialPath []string
	Follow      []grammar.Element
}

func remainingPathWith(nextDef []grammar.Element, targetDef []grammar.Element, i int) []grammar.Element {
	targetSlice := targetDef[i+1:]
	arr := make([]grammar.Element, 0, len(nextDef)+len(targetSlice))
	arr = append(arr, nextDef...)
	arr = append(arr, targetSlice...)
	return arr
}

func getAlternativesFor(grammr grammar.Grammar, result []PartialPathAndSuffixes, elements []grammar.Element, maxLength int, currPath []string) []PartialPathAndSuffixes {
	return append(result, possiblePathsFrom(grammr, maxLength, elements, currPath)...)
}

func IsOptionalCardinality(cardinality string) bool {
	return cardinality == CardinalityOptional || cardinality == CardinalityZeroOrMore
}

func possiblePathsFrom(grammr grammar.Grammar, maxLength int, elements []grammar.Element, currPath []string) []PartialPathAndSuffixes {
	result := []PartialPathAndSuffixes{}
	// Make a copy of currPath
	currPath = append([]string{}, currPath...)
	i := 0

	for len(currPath) < maxLength && i < len(elements) {
		element := elements[i]
		if IsOptionalCardinality(element.Cardinality()) {
			// Add path without this element
			result = getAlternativesFor(grammr, result, elements[i+1:], maxLength, currPath)
		}
		switch e := element.(type) {
		case grammar.Group:
			remain := remainingPathWith(e.Elements(), elements, i)
			return getAlternativesFor(grammr, result, remain, maxLength, currPath)
		case grammar.Keyword:
			currPath = append(currPath, GeneratedKeywordIdxName(e))
		case grammar.Alternatives:
			for _, alt := range e.Alts() {
				remain := remainingPathWith([]grammar.Element{alt}, elements, i)
				result = getAlternativesFor(grammr, result, remain, maxLength, currPath)
			}
			return result
		case grammar.Assignment:
			switch v := e.Value().(type) {
			case grammar.Keyword:
				currPath = append(currPath, GeneratedKeywordIdxName(v))
			case grammar.RuleCall:
				ruleRef := v.Rule().Ref(ctx.Background())
				switch rule := ruleRef.(type) {
				case grammar.Token:
					currPath = append(currPath, GeneratedTokenIdxName(rule))
				case grammar.AbstractRuleWithBody:
					remain := remainingPathWith([]grammar.Element{rule.Body()}, elements, i)
					result = getAlternativesFor(grammr, result, remain, maxLength, currPath)
					return result
				}
			case grammar.CrossRef:
				ruleRef := v.Rule().Rule().Ref(ctx.Background())
				switch rule := ruleRef.(type) {
				case grammar.Token:
					currPath = append(currPath, GeneratedTokenIdxName(rule))
				case grammar.AbstractRuleWithBody:
					remain := remainingPathWith([]grammar.Element{rule.Body()}, elements, i)
					result = getAlternativesFor(grammr, result, remain, maxLength, currPath)
					return result
				}
			case grammar.Alternatives:
				for _, alt := range v.Alts() {
					remain := remainingPathWith([]grammar.Element{alt}, elements, i)
					result = getAlternativesFor(grammr, result, remain, maxLength, currPath)
				}
				return result
			}
		case grammar.RuleCall:
			ruleRef := e.Rule().Ref(ctx.Background())
			switch rule := ruleRef.(type) {
			case grammar.Token:
				currPath = append(currPath, GeneratedTokenIdxName(rule))
			case grammar.AbstractRuleWithBody:
				remain := remainingPathWith([]grammar.Element{rule.Body()}, elements, i)
				result = getAlternativesFor(grammr, result, remain, maxLength, currPath)
				return result
			}
		}
		i++
	}

	result = append(result, PartialPathAndSuffixes{
		PartialPath: currPath,
		Follow:      elements[i:],
	})
	return result
}

type LookaheadPath []string
type LookaheadOption []LookaheadPath
type LLkLookahead []LookaheadOption

func GetLLkLookaheadOr(grammr grammar.Grammar, element grammar.Alternatives) LLkLookahead {
	return generateCommonLLkLookahead(grammr, element.Alts())
}

func generateCommonLLkLookahead(grammr grammar.Grammar, elements []grammar.Element) LLkLookahead {
	lookahead := LLkLookahead{}
	for i := range 3 {
		lookahead = LLkLookahead{}
		for _, alt := range elements {
			option := generateLookaheadOption(grammr, alt, i+1)
			lookahead = append(lookahead, option)
		}
		if isUniqueLookahead(lookahead) {
			break
		}
	}
	return lookahead
}

func generateLLkLookahead(node codegen.Node, name string, lookahead LLkLookahead) {
	node.AppendLine("var ", name, " = parser.LLkLookahead{")
	node.Indent(func(n codegen.Node) {
		for _, option := range lookahead {
			n.AppendLine("parser.LookaheadOption{")
			n.Indent(func(in codegen.Node) {
				for _, path := range option {
					in.AppendLine("parser.LookaheadPath{", strings.Join(path, ", "), "},")
				}
			})
			n.AppendLine("},")
		}
	})
	node.AppendLine("}")
}

func generateLookaheadOption(grammr grammar.Grammar, element grammar.Element, depth int) LookaheadOption {
	lookaheadOption := LookaheadOption{}
	partialPaths := possiblePathsFrom(grammr, depth, []grammar.Element{element}, []string{})
	for _, partialPath := range partialPaths {
		if len(partialPath.PartialPath) > 0 {
			lookaheadOption = append(lookaheadOption, partialPath.PartialPath)
		}
	}
	return lookaheadOption
}

func GetLLkLookaheadOpt(grammr grammar.Grammar, element grammar.Element) LLkLookahead {
	// Generate LL(1) decisions for options
	return LLkLookahead{
		generateLookaheadOption(grammr, element, 1),
	}
}

func isUniqueLookahead(lookahead LLkLookahead) bool {
	seen := make(map[string]bool)
	for _, option := range lookahead {
		localSeen := make(map[string]bool)
		for _, path := range option {
			key := strings.Join(path, ",")
			localSeen[key] = true
			if seen[key] {
				return false
			}
		}
		for key := range localSeen {
			seen[key] = true
		}
	}
	return true
}

func getInterfaceName(element grammar.Element) string {
	switch c := element.Container().(type) {
	case grammar.ParserRule:
		iface := grammar.FindReturnType(c, ctx.Background())
		return iface.Name()
	case grammar.Element:
		return getInterfaceName(c)
	default:
		panic("Unable to find parser rule for element")
	}
}
