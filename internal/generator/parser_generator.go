package generator

import (
	"slices"
	"strconv"

	core "typefox.dev/fastbelt"
	"typefox.dev/fastbelt/generator"
	"typefox.dev/fastbelt/internal/grammar"
	allstar "typefox.dev/fastbelt/parser/allstar"
)

type ParserGeneratorContext struct {
	grammar            grammar.Grammar
	accessNamesCounter map[string]int
	accessNames        map[core.AstNode]string
	// lookaheads and orLookaheads map grammar nodes to their ATN decision keys.
	// The keys are used verbatim in the generated predict / predictOpt calls.
	lookaheads   map[core.AstNode]string
	orLookaheads map[core.AstNode]string
	// Per-rule state for ATN key generation — reset per rule in populateContext.
	currentRule         string
	alternationCounter  int
	optionCounter       int
	repetitionCounter   int
	repMandatoryCounter int
}

func cardinalityToProdType(cardinality string) string {
	switch cardinality {
	case "?":
		return "Option"
	case "*":
		return "Repetition"
	case "+":
		return "RepetitionMandatory"
	default:
		return ""
	}
}

func (ctx *ParserGeneratorContext) nextOptKey(prodType string) string {
	switch prodType {
	case "Option":
		ctx.optionCounter++
		return ctx.currentRule + "_Option_" + strconv.Itoa(ctx.optionCounter)
	case "Repetition":
		ctx.repetitionCounter++
		return ctx.currentRule + "_Repetition_" + strconv.Itoa(ctx.repetitionCounter)
	case "RepetitionMandatory":
		ctx.repMandatoryCounter++
		return ctx.currentRule + "_RepetitionMandatory_" + strconv.Itoa(ctx.repMandatoryCounter)
	default:
		return ""
	}
}

func (context *ParserGeneratorContext) SetAccessName(node core.AstNode, name string) {
	index := context.accessNamesCounter[name]
	context.accessNames[node] = name + "_" + strconv.Itoa(index)
	index++
	context.accessNamesCounter[name] = index
}

func GenerateParser(grammr grammar.Grammar, packageName string) string {
	context := &ParserGeneratorContext{
		grammar:            grammr,
		accessNamesCounter: make(map[string]int),
		accessNames:        make(map[core.AstNode]string),
		lookaheads:         make(map[core.AstNode]string),
		orLookaheads:       make(map[core.AstNode]string),
	}
	populateContext(context)

	// Build the RuntimeATN at code-generation time.  Token IDs mirror the
	// assignment used by GenerateLexer so the emitted ATN and the lexer agree.
	rtn, err := BuildRuntimeATNFromGrammar(grammr)
	if err != nil {
		panic("fastbelt: ATN build failed: " + err.Error())
	}

	node := generator.NewNode()
	node.AppendLine("// Code generated by typefox.dev/fastbelt/cmd. DO NOT EDIT.")
	node.AppendLine()
	node.AppendLine("package ", packageName)
	node.AppendLine()
	node.AppendLine("import (")
	node.Indent(func(n generator.Node) {
		n.AppendLine(`allstar "typefox.dev/fastbelt/parser/allstar"`)
		n.AppendLine("core \"typefox.dev/fastbelt\"")
		n.AppendLine("\"typefox.dev/fastbelt/parser\"")
	})
	node.AppendLine(")")
	node.AppendLine()

	node.AppendLine("type Parser struct {")
	node.Indent(func(n generator.Node) {
		n.AppendLine("state    *parser.ParserState")
		n.AppendLine("srv      ", grammr.Name(), "GeneratedSrvCont")
		n.AppendLine("strategy parser.LookaheadStrategy")
	})
	node.AppendLine("}")
	node.AppendLine()

	node.AppendLine("func (p *Parser) references() ", grammr.Name(), "ReferencesConstructor {")
	node.Indent(func(n generator.Node) {
		n.AppendLine("return p.srv.", grammr.Name(), "Linking().ReferencesConstructor")
	})
	node.AppendLine("}").AppendLine()

	node.AppendLine("// WithLookaheadStrategy replaces the default ALL(*) strategy. Call before Parse.")
	node.AppendLine("func (p *Parser) WithLookaheadStrategy(s parser.LookaheadStrategy) *Parser {")
	node.Indent(func(n generator.Node) {
		n.AppendLine("p.strategy = s")
		n.AppendLine("return p")
	})
	node.AppendLine("}")
	node.AppendLine()

	node.AppendLine("func (p *Parser) predict(key string) int {")
	node.Indent(func(n generator.Node) {
		n.AppendLine("return p.strategy.Predict(p.state, key)")
	})
	node.AppendLine("}")
	node.AppendLine()

	node.AppendLine("func (p *Parser) predictOpt(key string) bool {")
	node.Indent(func(n generator.Node) {
		n.AppendLine("return p.strategy.PredictOpt(p.state, key)")
	})
	node.AppendLine("}")
	node.AppendLine()

	rules := grammr.Rules()
	if len(rules) == 0 {
		return FormatIfPossible(node.String())
	}

	firstRule := rules[0]
	node.AppendLine("func (p *Parser) Parse(document *core.Document) *parser.ParseResult {")
	node.Indent(func(n generator.Node) {
		n.AppendLine("cp := &Parser{srv: p.srv, strategy: p.strategy, state: parser.NewParserState(document.Tokens)}")
		n.AppendLine("result := cp.Parse", firstRule.Name(), "()")
		n.AppendLine("core.AssignContainers(document, result)")
		n.AppendLine("return &parser.ParseResult{Node: result, Errors: cp.state.Errors()}")
	})
	node.AppendLine("}")
	node.AppendLine()

	// Access-name constants (iota block, sorted for determinism).
	node.AppendLine("const (")
	accessIota := true
	accessNames := make([]string, 0, len(context.accessNames))
	node.Indent(func(access generator.Node) {
		for _, name := range context.accessNames {
			accessNames = append(accessNames, name)
		}
		slices.Sort(accessNames)
		for _, name := range accessNames {
			if accessIota {
				access.AppendLine(name + " = iota + 1")
				accessIota = false
			} else {
				access.AppendLine(name)
			}
		}
	})
	node.AppendLine(")")
	node.AppendLine()

	// NewParser uses the emitted ATN with the ALL(*) prediction engine.
	node.AppendLine("func NewParser(srv ", grammr.Name(), "GeneratedSrvCont) *Parser {")
	node.Indent(func(n generator.Node) {
		n.AppendLine("return &Parser{")
		n.Indent(func(in generator.Node) {
			in.AppendLine("srv: srv,")
			in.AppendLine("strategy: allstar.NewLLStarLookaheadFromRuntime(buildATN(), nil).AsParserStrategy(),")
		})
		n.AppendLine("}")
	})
	node.AppendLine("}")
	node.AppendLine()

	// Emit the pre-built RuntimeATN as a private constructor function.
	node.AppendNode(allstar.EmitATNFunc("buildATN", "allstar", rtn))
	node.AppendLine()

	for _, rule := range grammr.Rules() {
		generateParseFunction(node, context, rule)
	}

	return FormatIfPossible(node.String())
}

func populateContext(context *ParserGeneratorContext) {
	for _, rule := range context.grammar.Rules() {
		ruleName := rule.Name()
		context.currentRule = ruleName
		context.alternationCounter = 0
		context.optionCounter = 0
		context.repetitionCounter = 0
		context.repMandatoryCounter = 0
		populateContextWithNode(context, ruleName, rule.Body())
	}
}

// TODO: use the prefix with first letter lowercase for global constants and variables to keep them private
func populateContextWithNode(context *ParserGeneratorContext, prefix string, node core.AstNode) {
	if _, exists := context.accessNames[node]; exists {
		return
	}
	switch n := node.(type) {
	case grammar.Alternatives:
		if len(n.Alts()) > 1 {
			context.alternationCounter++
			atnKey := context.currentRule + "_Alternation_" + strconv.Itoa(context.alternationCounter)
			context.orLookaheads[n] = atnKey
		}
		if n.Cardinality() != CardinalityOne {
			prodType := cardinalityToProdType(n.Cardinality())
			context.lookaheads[n] = context.nextOptKey(prodType)
		}
		for _, alt := range n.Alts() {
			populateContextWithNode(context, prefix, alt)
		}
	case grammar.Group:
		if n.Cardinality() != CardinalityOne {
			prodType := cardinalityToProdType(n.Cardinality())
			context.lookaheads[n] = context.nextOptKey(prodType)
		}
		for _, element := range n.Elements() {
			populateContextWithNode(context, prefix, element)
		}
	case grammar.Keyword:
		if n.Cardinality() != CardinalityOne {
			prodType := cardinalityToProdType(n.Cardinality())
			context.lookaheads[n] = context.nextOptKey(prodType)
		}
		name := prefix + KeywordName(n)
		context.SetAccessName(node, name)
	case grammar.Assignment:
		if n.Cardinality() != CardinalityOne {
			prodType := cardinalityToProdType(n.Cardinality())
			context.lookaheads[n] = context.nextOptKey(prodType)
		}
		name := prefix + n.Property().Text
		populateContextWithNode(context, name, n.Value())
	case grammar.CrossRef:
		populateContextWithNode(context, prefix, n.Rule())
	case grammar.RuleCall:
		if n.Cardinality() != CardinalityOne {
			prodType := cardinalityToProdType(n.Cardinality())
			context.lookaheads[n] = context.nextOptKey(prodType)
		}
		token := getTokenWithName(context.grammar, n.Rule().Text)
		if token != nil {
			name := prefix + token.Name()
			context.SetAccessName(node, name)
		}
	}
}

func generateParseFunction(node generator.Node, context *ParserGeneratorContext, rule grammar.ParserRule) {
	node.AppendLine("func (p *Parser) Parse", rule.Name(), "() ", rule.ReturnType().Text, " {")
	node.Indent(func(n generator.Node) {
		n.AppendLine("current := New", rule.ReturnType().Text, "()")
		n.AppendLine("current.SetSegmentStartToken(p.state.LA(1))")
		generateAbstractElementParser(n, context, rule.Body())
		n.AppendLine("current.SetSegmentEndToken(p.state.LA(0))")
		n.AppendLine("return current")
	})
	node.AppendLine("}")
	node.AppendLine()
}

func generateAbstractElementParser(node generator.Node, context *ParserGeneratorContext, element grammar.Element) {
	if alts, ok := element.(grammar.Alternatives); ok {
		generateAlternativesParser(node, context, alts)
	} else if group, ok := element.(grammar.Group); ok {
		generateGroupParser(node, context, group)
	} else if action, ok := element.(grammar.Action); ok {
		node.AppendLine("{")
		node.Indent(func(n generator.Node) {
			n.AppendLine("result := New", action.Type().Text, "()")
			// Inherit segment from previous node
			n.AppendLine("result.SetSegment(current.Segment())")
			if action.Property() != nil {
				if action.Operator() == "+=" {
					n.AppendLine("result.Set", action.Property().Text, "Item(current)")
				} else {
					n.AppendLine("result.Set", action.Property().Text, "(current)")
				}
				// Ensure that the previous node has a valid segment ending
				n.AppendLine("current.SetSegmentEndToken(p.state.LA(0))")
				n.AppendLine("current = result")
			} else {
				// If there is no property to assign, just merge tokens
				n.AppendLine("core.AssignTokens(result, current.Tokens())")
				n.AppendLine("current = result")
			}
		})
		node.AppendLine("}")
		node.AppendLine("current := current.(", action.Type().Text, ")")
	} else {
		node.AppendLine("{")
		node.Indent(func(indent generator.Node) {
			if keyword, ok := element.(grammar.Keyword); ok {
				generateKeywordParser(indent, context, keyword)
			} else if ruleCall, ok := element.(grammar.RuleCall); ok {
				resultName := generateRuleCallParser(indent, context, ruleCall)
				if resultName == "result" {
					// Unassigned rule call result
					// Needs to be merged into current node
					indent.AppendLine("core.MergeTokens(result, current.Tokens())")
					indent.AppendLine("current = result")
				}
			} else if assignment, ok := element.(grammar.Assignment); ok {
				generateCardinality(indent, func(n generator.Node) {
					generateAssignable(n, context, assignment.Value(), func(n2 generator.Node, resultName string) {
						n2.AppendLine("if ", resultName, " != nil {")
						if _, ok := assignment.Value().(grammar.CrossRef); ok {
							parserRuleName := getParserRuleName(assignment)
							// For cross-references, we need to create a Reference object
							resultName = "p.references()." + parserRuleName + assignment.Property().Text + "(current, " + resultName + ")"
						}
						n2.Indent(func(in generator.Node) {
							switch assignment.Operator() {
							case "+=":
								// Append to slice
								in.AppendLine("current.Set", assignment.Property().Text, "Item(", resultName, ")")
							default:
								// Single assignment
								in.AppendLine("current.Set", assignment.Property().Text, "(", resultName, ")")
							}
						})
						n2.AppendLine("}")
					})
				}, func(n generator.Node) {
					n.Append(generatePredictOptExpr(context.lookaheads[element]))
				}, element.Cardinality())
			}
		})
		node.AppendLine("}")
	}
}

func generateAssignable(node generator.Node, context *ParserGeneratorContext, assignable grammar.Assignable, cb func(node generator.Node, resultName string)) {
	if crossRef, ok := assignable.(grammar.CrossRef); ok {
		resultName := generateCrossReferenceParser(node, context, crossRef)
		cb(node, resultName)
	} else if keyword, ok := assignable.(grammar.Keyword); ok {
		resultName := generateKeywordParser(node, context, keyword)
		cb(node, resultName)
	} else if ruleCall, ok := assignable.(grammar.RuleCall); ok {
		resultName := generateRuleCallParser(node, context, ruleCall)
		cb(node, resultName)
	} else if alts, ok := assignable.(grammar.Alternatives); ok {
		generateAssignableAlternatives(node, context, alts, cb)
	} else {
		panic("Unresolved assignment assignable")
	}
}

func generateAssignableAlternatives(node generator.Node, context *ParserGeneratorContext, alts grammar.Alternatives, cb func(node generator.Node, resultName string)) {
	node.AppendLine("switch p.predict(\"", context.orLookaheads[alts], "\") {")
	for i, alt := range alts.Alts() {
		node.AppendLine("case ", strconv.Itoa(i), ":")
		node.Indent(func(in generator.Node) {
			if assignable, ok := alt.(grammar.Assignable); ok {
				generateAssignable(in, context, assignable, cb)
			}
		})
	}
	node.AppendLine("}")
}

func generateCrossReferenceParser(node generator.Node, context *ParserGeneratorContext, crossRef grammar.CrossRef) string {
	return generateRuleCallParser(node, context, crossRef.Rule())
}

func generateGroupParser(node generator.Node, context *ParserGeneratorContext, group grammar.Group) {
	generateCardinality(node, func(n generator.Node) {
		for _, element := range group.Elements() {
			generateAbstractElementParser(n, context, element)
		}
	}, func(n generator.Node) {
		n.Append(generatePredictOptExpr(context.lookaheads[group]))
	}, group.Cardinality())
}

func generateKeywordParser(node generator.Node, context *ParserGeneratorContext, keyword grammar.Keyword) string {
	lookahead := "p.state.LA(1) == " + GeneratedKeywordIdxName(keyword)
	generateCardinality(node, func(n generator.Node) {
		n.AppendLine("token := p.state.Consume(", GeneratedKeywordIdxName(keyword), ")")
		n.AppendLine("core.AssignToken(current, token, ", context.accessNames[keyword], ")")
	}, func(n generator.Node) { n.Append(lookahead) }, keyword.Cardinality())
	return "token"
}

func generateRuleCallParser(node generator.Node, context *ParserGeneratorContext, ruleCall grammar.RuleCall) string {
	token := getTokenWithName(context.grammar, ruleCall.Rule().Text)
	rule := getRuleWithName(context.grammar, ruleCall.Rule().Text)
	lookahead := generatePredictOptExpr(context.lookaheads[ruleCall])
	var result string
	if token != nil {
		result = "token"
	} else if rule != nil {
		result = "result"
	} else {
		panic("Unresolved rule call: " + ruleCall.Rule().Text)
	}
	first := true
	generateCardinality(node, func(n generator.Node) {
		eq := "="
		if first {
			eq = ":="
			first = false
		}
		if token != nil {
			n.AppendLine("token ", eq, " p.state.Consume(", GeneratedTokenIdxName(token), ")")
			n.AppendLine("core.AssignToken(current, token, ", context.accessNames[ruleCall], ")")
		} else if rule != nil {
			n.AppendLine("result ", eq, " p.Parse", rule.Name(), "()")
		}
	}, func(n generator.Node) { n.Append(lookahead) }, ruleCall.Cardinality())
	return result
}

func generatePredictOptExpr(atnKey string) string {
	return "p.predictOpt(\"" + atnKey + "\")"
}

func generateAlternativesParser(node generator.Node, context *ParserGeneratorContext, alts grammar.Alternatives) {
	orAtnKey := context.orLookaheads[alts]
	generateCardinality(node, func(n generator.Node) {
		n.AppendLine("switch p.predict(\"", orAtnKey, "\") {")
		for i, alt := range alts.Alts() {
			n.AppendLine("case ", strconv.Itoa(i), ":")
			n.Indent(func(in generator.Node) {
				generateAbstractElementParser(in, context, alt)
			})
		}
		n.AppendLine("}")
	}, func(n generator.Node) {
		n.Append(generatePredictOptExpr(context.lookaheads[alts]))
	}, alts.Cardinality())
}

func generateCardinality(node generator.Node, element, lookahead generator.Callback, cardinality string) {
	switch cardinality {
	case CardinalityOne:
		element(node)
	case CardinalityOptional:
		node.Append("if ")
		lookahead(node)
		node.AppendLine(" {")
		node.Indent(element)
		node.AppendLine("}")
	case CardinalityZeroOrMore:
		node.Append("for ")
		lookahead(node)
		node.AppendLine(" {")
		node.Indent(element)
		node.AppendLine("}")
	case CardinalityOneOrMore:
		node.Append("for ok := true; ok; ok = ")
		lookahead(node)
		node.AppendLine(" {")
		node.Indent(element)
		node.AppendLine("}")
	}
}

func getTokenWithName(grammr grammar.Grammar, name string) grammar.Token {
	for _, t := range grammr.Terminals() {
		if t.Name() == name {
			return t
		}
	}
	return nil
}

func getRuleWithName(grammr grammar.Grammar, name string) grammar.ParserRule {
	for _, r := range grammr.Rules() {
		if r.Name() == name {
			return r
		}
	}
	return nil
}

func getParserRuleName(element grammar.Element) string {
	cont := element.Container()
	if rule, ok := cont.(grammar.ParserRule); ok {
		return rule.Name()
	} else if parent, ok := cont.(grammar.Element); ok {
		return getParserRuleName(parent)
	} else {
		panic("Unable to find parser rule for element")
	}
}
