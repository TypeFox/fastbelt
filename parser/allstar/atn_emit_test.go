func TestEmitGoSource_OptionalFields(t *testing.T) {
	s0 := &RuntimeATNState{
		StateNumber:            0,
		Type:                   ATNStarLoopEntry,
		EpsilonOnlyTransitions: true,
		RuleName:               "MyRule",
		ProdKind:               ProdAlternation,
		ProdIdx:                2,
	}
	rtn := &RuntimeATN{
		States:         []*RuntimeATNState{s0},
		DecisionStates: []*RuntimeATNState{s0},
		DecisionMap:    map[string]*RuntimeATNState{"MyRule_Alternation_2": s0},
	}
	src := EmitGoSource("mypkg", "BuildATN", "typefox.dev/fastbelt/parser/allstar", rtn)

	assert.Contains(t, src, "EpsilonOnlyTransitions: true,")
	assert.Contains(t, src, `RuleName: "MyRule",`)
	assert.Contains(t, src, "ProdKind: allstar.ProdAlternation,")
	assert.Contains(t, src, "ProdIdx: 2,")
}

// TestEmitGoSource_OptionalFieldsAbsent verifies that optional fields are not emitted
// when they hold their zero values.
func TestEmitGoSource_OptionalFieldsAbsent(t *testing.T) {
	src := EmitGoSource("mypkg", "BuildATN", "typefox.dev/fastbelt/parser/allstar", minimalATN())

	assert.NotContains(t, src, "EpsilonOnlyTransitions")
	assert.NotContains(t, src, "RuleName")
	assert.NotContains(t, src, "ProdKind")
	assert.NotContains(t, src, "ProdIdx")
}

